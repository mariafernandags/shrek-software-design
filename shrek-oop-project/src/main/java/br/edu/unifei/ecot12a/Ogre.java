package br.edu.unifei.ecot12a;

public class Ogre extends Being{
    private float strenght;
    private String intimidation;
    private String color;

    public float getStrenght() {
        return strenght;
    }
    public void setStrenght(float strenght) {
        this.strenght = strenght;
    }
    public String getIntimidation() {
        return intimidation;
    }
    public void setIntimidation(String intimidation) {
        this.intimidation = intimidation;
    }
    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }
}
