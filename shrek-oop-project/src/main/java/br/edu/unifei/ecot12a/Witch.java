package br.edu.unifei.ecot12a;

public class Witch extends Human {
    private String broomType;
    private String skinColor;
    private int pomegranadeApple;
    private String laugh;
    private String animalCompanion;
    public String getBroomType() {
        return broomType;
    }
    public void setBroomType(String broomType) {
        this.broomType = broomType;
    }
    public String getSkinColor() {
        return skinColor;
    }
    public void setSkinColor(String skinColor) {
        this.skinColor = skinColor;
    }
    public int getPomegranadeApple() {
        return pomegranadeApple;
    }
    public void setPomegranadeApple(int pomegranadeApple) {
        this.pomegranadeApple = pomegranadeApple;
    }
    public String getLaugh() {
        return laugh;
    }
    public void setLaugh(String laugh) {
        this.laugh = laugh;
    }
    public String getAnimalCompanion() {
        return animalCompanion;
    }
    public void setAnimalCompanion(String animalCompanion) {
        this.animalCompanion = animalCompanion;
    }
}
